# 문자열 슬라이싱(slicing): 문자열 잘라내기
# 특정한 범위를 지정해서 추출
# 원본의 변화는 없다.
# 추출된 문자열은 새로운 변수에 할당
#
# 변수(문자열) = 문자열[시작번호:종료번호:스텝]
# 시작번호 : 0부터 시작
# 종료번호 : 종료번호 - 1까지 참조
# 스텝간격 : 건너뛰기

#%%

s ="012345678901234567890"
sl = len(s)

# 스텝: 기본값 1칸
s0 = s[::]  # 생략가능
s1 = s[::1]
s2 = s[:]
print(s0)   # 012345678901234567890
print(s1)   # 012345678901234567890
print(s2)   # 012345678901234567890


#%%

# 처음부터 끝까지 2칸 간격으로 건너뛰기
s2 = s[::2]
print(s2) # 02468024680

#%%
s3= s[2::2]
print(s3)
#%%
sn ="12345678901234567890"
sl = len(sn)
sn0 = sn[::2]
sn1 = sn[1::2]
print("홀수자리숫자 :",sn0)
print("짝수자리숫자 :",sn1)
print(f"전체문자열{sn}에서 홀수는{sn0}이고, 짝수는{sn1}이다.")
#%%
# 규칙 없이 마구 섞인 문자열 데이터
random_numbers = "730294156828491"

odds = []   # 홀수를 담을 빈 바구니
evens = []  # 짝수를 담을 빈 바구니

# for문을 짜서 숫자를 한 글자씩 차례대로 꺼냅니다.
for char in random_numbers:
    num = int(char)  # 글자 형태를 계산이 가능한 '진짜 숫자'로 바꿉니다.
    
    # % 연산자는 나눈 후 '나머지'를 구합니다. 2로 나눈 나머지가 1이면 무조건 홀수입니다.
    if num % 2 == 1: 
        odds.append(char)  # 홀수 바구니에 넣기
    else:            
        evens.append(char) # 짝수 바구니에 넣기 (0도 짝수로 분류됩니다)

# 결과를 보기 좋게 합쳐서 출력합니다.
print("원래 숫자:", random_numbers)
print("구별된 홀수:", "".join(odds))
print("구별된 짝수:", "".join(evens))