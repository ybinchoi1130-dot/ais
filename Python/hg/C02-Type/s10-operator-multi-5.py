# 복합 대입연산자(축약형)
# /= : 나눠서 대입(부동 소수점)

"""
# 오차 발생
# IEEE 754 규약
"""

#%%

a = 10
b = 3
print('a=', a)
print('b=', b)

c = a / b
print(f"{c} = {a} / {b}")
# 3.3333333333333335 = 10 / 3


#%%

# /= : 나눠서 대입(부동 소수점) : b = b / a
bx = b
b /= a  # b <- 3 / 10
print('b /= a : ', b) # 0.3
print(f"{bx} /= {a} -> {b}") # 3 /= 10 -> 0.3

