# x와 y의 값을 바꾸려면?
x = 10
y = 20
print('x=', x) # x= 10
print('y=', y) # y= 20

# 파이썬 방식
print('x와 y값의 교환')
x, y = y, x
print('x=', x) # x= 20
print('y=', y) # y= 10

