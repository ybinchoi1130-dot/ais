# 함수호출
# 함수이름(표현식)
# print(표현식)
# 표현식(expression)

#%%

print("10 + 20")  # 10 + 20
print(10 + 20)    # 30
print(10)         # 10
print(20)         # 20

#%%

a = 10    # 할당: 숫자 10을 변수 a에 저장
b = 20    # 할당: 숫자 20을 변수 b에 저장

# 변수 a와 b에 있는 값을 더한 후 그 결과를 
# 변수 c에 할당
c = a + b 

print(c)  # 30

#%%

print(a, b, c) # 10, 20, 30


